print("*** โปรแกรมคำนวณดอกเบี้ยบัญชีออมทรัพย์ ***")

# รับจำนวนบัญชี
จำนวนบัญชี = int(input("จำนวนบัญชีออมทรัพย์: "))

# ตัวแปรสำหรับสะสมดอกเบี้ยรวม
ดอกเบี้ยรวม = 0

# วนลูปคำนวณแต่ละบัญชี
for ลำดับ in range(1, จำนวนบัญชี + 1):
    เงินต้น = float(input(f"ป้อนเงินต้นของบัญชีที่ {ลำดับ} (บาท): "))
    อัตราดอกเบี้ย = float(input(f"ป้อนอัตราดอกเบี้ยของบัญชีที่ {ลำดับ} (ตัวอย่าง: 0.05 สำหรับ 5%): "))
    ดอกเบี้ย = เงินต้น * อัตราดอกเบี้ย
    ดอกเบี้ยรวม += ดอกเบี้ย
    print(f"ดอกเบี้ยของบัญชีที่ {ลำดับ}: {ดอกเบี้ย:.2f} บาท\n")

# แสดงผลรวม
print(f"ดอกเบี้ยรวมจากทุกบัญชี: {ดอกเบี้ยรวม:.2f} บาท")
