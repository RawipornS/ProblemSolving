# เริ่มต้นการคำนวณพื้นที่สามเหลี่ยม 10 รูป
print("โปรแกรมคำนวณพื้นที่สามเหลี่ยมจำนวน 10 รูป")

# ตั้งค่าตัวนับเริ่มต้น
counter = 1

# วนลูปเพื่อคำนวณพื้นที่สามเหลี่ยม 10 รูป
while counter <= 10:
    print(f"\nรูปที่ {counter}")
    
    # รับค่าฐานและความสูงจากผู้ใช้
    x = float(input("กรอกความยาวฐานของสามเหลี่ยม (X): "))
    y = float(input("กรอกความสูงของสามเหลี่ยม (Y): "))
    
    # คำนวณพื้นที่
    area = (x * y) / 2
    
    # แสดงผลลัพธ์
    print(f"พื้นที่ของสามเหลี่ยมคือ: {area:.2f}")
    
    # เพิ่มค่าตัวนับ
    counter += 1

print("\nจบการทำงาน")
