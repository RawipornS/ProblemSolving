print("*** โปรแกรมคำนวณเงินฝากบัญชีออมทรัพย์ ***")
# รับจำนวนบัญชีออมทรัพย์
จำนวนบัญชี = int(input("กรุณาป้อนจำนวนบัญชีออมทรัพย์: "))

# ตัวแปรสำหรับเก็บข้อมูลบัญชี
บัญชีทั้งหมด = []

# วนลูปเพื่อรับข้อมูลแต่ละบัญชี
for ลำดับ in range(1, จำนวนบัญชี + 1):
    print(f"\n--- บัญชีที่ {ลำดับ} ---")
    เงินต้น = float(input(f"ป้อนเงินต้นของบัญชีที่ {ลำดับ} (บาท): "))
    อัตราดอกเบี้ย = float(input(f"ป้อนอัตราดอกเบี้ยของบัญชีที่ {ลำดับ} (ตัวอย่าง: 0.05 สำหรับ 5%): "))
    # คำนวณดอกเบี้ย
    ดอกเบี้ย = เงินต้น * อัตราดอกเบี้ย
    
    # เก็บข้อมูลบัญชีในลิสต์
    บัญชีทั้งหมด.append({
        "ลำดับบัญชี": ลำดับ,
        "เงินต้น": เงินต้น,
        "อัตราดอกเบี้ย": อัตราดอกเบี้ย,
        "ดอกเบี้ย": ดอกเบี้ย
    })
# แสดงผลลัพธ์
print("\n--- สรุปรายการบัญชีออมทรัพย์ ---")
ดอกเบี้ยรวม = 0
for บัญชี in บัญชีทั้งหมด:
    print(f"บัญชีที่ {บัญชี['ลำดับบัญชี']}:")
    print(f"  เงินต้น: {บัญชี['เงินต้น']} บาท")
    print(f"  อัตราดอกเบี้ย: {บัญชี['อัตราดอกเบี้ย'] * 100:.2f}%")
    print(f"  ดอกเบี้ย: {บัญชี['ดอกเบี้ย']} บาท")
    ดอกเบี้ยรวม += บัญชี["ดอกเบี้ย"]

print(f"\nดอกเบี้ยรวมจากทุกบัญชี: {ดอกเบี้ยรวม} บาท")
